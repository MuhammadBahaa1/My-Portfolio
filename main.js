/* ----- CHANGE ACTIVE LINK ----- */

const sections = document.querySelectorAll('section[id]')

function scrollActive() {
  const scrollY = window.scrollY;

  sections.forEach(current =>{
    const sectionHeight = current.offsetHeight,
        sectionTop = current.offsetTop - 50,
      sectionId = current.getAttribute('id')

    if(scrollY > sectionTop && scrollY <= sectionTop + sectionHeight) { 

        document.querySelector('.nav-menu a[href*=' + sectionId + ']').classList.add('active-link')

    }  else {

      document.querySelector('.nav-menu a[href*=' + sectionId + ']').classList.remove('active-link')

    }
  })
}

window.addEventListener('scroll', scrollActive)

let slideIndex = 0;

function openModal(modalId) {
    document.getElementById(modalId).style.display = "block";
    slideIndex = 0; // Reset slide index
    showSlides(modalId, slideIndex);
}

function closeModal(modalId) {
    document.getElementById(modalId).style.display = "none";
}

function nextSlide(modalId) {
    slideIndex++;
    showSlides(modalId, slideIndex);
}

function prevSlide(modalId) {
    slideIndex--;
    showSlides(modalId, slideIndex);
}

function showSlides(modalId, n) {
    let modal = document.getElementById(modalId);
    let images = modal.getElementsByClassName("modal-image");
    let videos = modal.getElementsByClassName("modal-video");

    // Total media items
    const totalImages = images.length;
    const totalVideos = videos.length;
    const totalMedia = totalImages + totalVideos;

    // Wrap around logic
    if (n >= totalMedia) { slideIndex = 0; }
    if (n < 0) { slideIndex = totalMedia - 1; }

    // Hide all media
    for (let img of images) {
        img.style.display = "none";
    }
    for (let video of videos) {
        video.style.display = "none";
    }

    // Show the appropriate media
    if (slideIndex < totalImages) {
        images[slideIndex].style.display = "block"; // Show image
    } else {
        videos[slideIndex - totalImages].style.display = "block"; // Show video
    }
}
